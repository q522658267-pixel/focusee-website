function uniqueList(items){
var out=[],seen={};
for(var i=0;i<items.length;i++){
var raw=items[i];
if(raw===undefined||raw===null) continue;
var item=String(raw).replace(/\s+/g,' ').trim();
if(!item) continue;
var key=item.toLowerCase();
if(seen[key]) continue;
seen[key]=true;
out.push(item);
}
return out;
}
