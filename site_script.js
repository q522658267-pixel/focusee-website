function normalizeName(name){
return (name||'').replace(/\s+V\d+$/i,'').replace(/\s+Version\s*\d+$/i,'').trim().toLowerCase();
}
function getDetailGallery(product){
var gallery=[];
var seen={};
function pushItem(item){
if(!item||!item.img||seen[item.img]) return;
seen[item.img]=true;
gallery.push(item);
}
for(var i=0;i<ALL_PRODUCTS.length;i++){
if(ALL_PRODUCTS[i].model===product.model) pushItem(ALL_PRODUCTS[i]);
}
if(gallery.length<2){
var baseName=normalizeName(product.name);
for(var j=0;j<ALL_PRODUCTS.length;j++){
if(normalizeName(ALL_PRODUCTS[j].name)===baseName) pushItem(ALL_PRODUCTS[j]);
}
}
if(!gallery.length) pushItem(product);
if(gallery[0].img!==product.img){
for(var k=0;k<gallery.length;k++){
if(gallery[k].img===product.img){
var current=gallery.splice(k,1)[0];
gallery.unshift(current);
break;
}
}
}
return gallery;
}
